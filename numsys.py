# binary number
# implicit
a = 0b1111
print(a)
print(type(a))
# explicit
a = bin(0b1110)
print(a)
print(type(a))
# data type annotation
a =bin = 0b11100
print(a)
print(type(a))
print("\n")
# octal number system
# implicit
a = 0o1342
print(a)
print(type(a))
# explicit
a = oct(0o4322)
print(a)
print(type(a))
# data type annotation
a = oct = 0o11342
print(a)
print(type(a))
print("\n")
